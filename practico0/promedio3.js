// Se declaran tres variables, "promedio_1", "promedio_2" y "promedio_3", para almacenar los promedios de cada conjunto de valores.
// Cada promedio se obtiene sumando todos los valores en un conjunto y luego dividiendo esa suma entre la cantidad de valores en ese conjunto.

